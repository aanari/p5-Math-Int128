
typedef __int128 int128_t;

int128_t
add128(int128_t a, int128_t b) {
  return a + b;
}

int128_t
sub128(int128_t a, int128_t b) {
  return a - b;
}

int128_t
mul128(int128_t a, int128_t b) {
  return a * b;
}

int128_t
div128(int128_t a, int128_t b) {
  return a / b;
}

int128_t
rem128(int128_t a, int128_t b) {
  return a % b;
}

